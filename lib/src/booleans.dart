/// Returns `true` if the boolean [value] is `false`
///
/// `since 0.0.1`
bool isFalse(bool? value) => value == false;

/// Returns `true` if the boolean [value] is `true`
///
/// `since 0.0.1`
bool isTrue(bool? value) => value == true;
